const { ethers } = require("ethers");


async function getBalance() {
  try {
    // Contract'ı oluşturun
    const accountAddress = "0xAE72602cfbE91fDfb07f889Eb74F60ADdaEa2b46";
    const provider = new ethers.providers.JsonRpcProvider("https://eth-sepolia.g.alchemy.com"); // Ethereum ağ sağlayıcısı (örnek olarak SePolia Test Ağı)

    const contract = new ethers.Contract(contractAddress, contractABI, provider);

    // balanceOf fonksiyonunu çağırın ve sonucu alın
    const balance = await contract.balanceOf(accountAddress);

    console.log("Balance:", balance.toString());
  } catch (error) {
    console.error("Error:", error);
  }
}

async function listContractFunctions(contractAddress, abi) {
  const provider = new ethers.providers.JsonRpcProvider("https://eth-sepolia.g.alchemy.com"); // Ethereum ağ sağlayıcısı (örnek olarak SePolia Test Ağı)
  const contract = new ethers.Contract(contractAddress, abi, provider);

  console.log("Contract Functions:");
  for (const functionName in contract.functions) {
    console.log(functionName);
  }
}

